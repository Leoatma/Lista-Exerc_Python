'''
07. Faça um Programa que calcule a área de um quadrado, em seguida mostre o dobro desta área para o usuário.
'''

ladoQuadrado = int(input('Informe o tamanho do lado do quadrado: '))

areaQuadrado = ladoQuadrado ** 2

print(f'A área do quadrado é {areaQuadrado}, e seu dobro é {areaQuadrado * 2}')